package oop;

public class Student {          // using student class for the template and then will set the data in another class and
    // the will use the object there to call these methods


    // OOP means object oriented programming
    // OOP concepts are:
    // - encapsulation
    //- polymorphism
    //- inheritance
    //- abstraction


    String name;


    // use setter method :
    public void setnameofstudent(String name) {
        this.name = name;


    }

    // getter method
    public String getnameofstudent() {
        return name;


    }


}
